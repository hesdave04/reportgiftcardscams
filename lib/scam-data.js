/* ── Slug helper ────────────────────────────────────────────────── */
export function stateToSlug(name) {
  return name.toLowerCase().replace(/\s+/g, "-").replace(/[^a-z-]/g, "");
}

export function slugToState(slug) {
  const all = STATES_TOTAL.map((s) => s.st);
  return all.find((name) => stateToSlug(name) === slug) || null;
}

/* ── Get all state slugs ────────────────────────────────────────── */
export function getAllStateSlugs() {
  return STATES_TOTAL.map((s) => stateToSlug(s.st));
}

/* ── Get state data bundle ──────────────────────────────────────── */
export function getStateData(stateName) {
  const total = STATES_TOTAL.find((s) => s.st === stateName);
  const percap = STATES_PERCAP.find((s) => s.st === stateName);
  const abbrev = STATE_ABBREV[stateName] || "";
  const pop = STATE_POP[stateName] || 0;

  // Find metros in this state
  const stateMetros = METROS
    .map(([metro, states], i) => ({ metro, states, rank: i + 1 }))
    .filter(({ states }) => {
      const parts = states.split("-");
      return parts.includes(abbrev);
    });

  // Compute YoY change
  const yoyChange = total ? ((total.y25 - total.y24) / total.y24 * 100) : 0;
  const percapYoyChange = percap ? ((percap.y25 - percap.y24) / percap.y24 * 100) : 0;

  // National totals for context
  const nationalTotal25 = STATES_TOTAL.reduce((sum, s) => sum + s.y25, 0);
  const shareOfNational = total ? (total.y25 / nationalTotal25 * 100) : 0;

  return {
    name: stateName,
    abbrev,
    pop,
    total,
    percap,
    metros: stateMetros,
    yoyChange,
    percapYoyChange,
    shareOfNational,
    nationalTotal25,
  };
}

/* ── Format helpers ─────────────────────────────────────────────── */
export function fmt(n) {
  if (n >= 1e9) return "$" + (n / 1e9).toFixed(2) + "B";
  if (n >= 1e6) return "$" + (n / 1e6).toFixed(1) + "M";
  return "$" + Number(n).toLocaleString("en-US");
}

export function fmtFull(n) {
  return "$" + Number(n).toLocaleString("en-US");
}

export function fmtPop(n) {
  if (n >= 1e6) return (n / 1e6).toFixed(1) + "M";
  return Number(n).toLocaleString("en-US");
}
